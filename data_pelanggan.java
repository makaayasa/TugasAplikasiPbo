    private void btn_simpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_simpanActionPerformed
    String Kategori = null;
    if(rexpress.isSelected()){
        Kategori = "Express";
    }else if(rreguler.isSelected()){
        Kategori ="Reguler";
    }
    String sql = "Insert into pelanggan values(?,?,?,?,?,?,?)";
    try{
        PreparedStatement stat = conn.prepareStatement(sql);
        stat.setString(1,txt_nomor.getText());
        stat.setString(2,txt_nama.getText());
        stat.setString(3,Kategori);
        stat.setString(4,txt_berat.getText());
        stat.setString(5,txt_alamat.getText());
        stat.setString(6,txt_status.getText());
        stat.setString(7,txt_tagihan.getText());
        
        stat.executeUpdate();
        JOptionPane.showMessageDialog(null,"Data tersimpan");
        kosong();
        txt_nomor.requestFocus();
    }
    catch (SQLException e){
        JOptionPane.showMessageDialog(null,"Data belum terinput semua /diupdate");
   
       
    }
        datatable();
    }//GEN-LAST:event_btn_simpanActionPerformed

