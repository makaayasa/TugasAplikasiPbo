        String a= tblpelanggan.getValueAt(row, 0).toString();
        String b= tblpelanggan.getValueAt(row, 1).toString();
        String c= tblpelanggan.getValueAt(row, 2).toString();
        String d= tblpelanggan.getValueAt(row, 3).toString();
        String e= tblpelanggan.getValueAt(row, 4).toString();
        String f= tblpelanggan.getValueAt(row, 5).toString();
        String g= tblpelanggan.getValueAt(row, 6).toString();
        
        
        txt_nomor.setText(a);
        txt_nama.setText(b);
